// ── Scene definitions (interaction → mini-cutscene) ──────────
export type SceneId = "feed" | "play" | "wash" | "walk" | "pet" | "treat" | "sleep" | "train";

export type SceneOverlay = "none" | "bath" | "park" | "dark";

export type Scene = {
  id: SceneId;
  durationMs: number;
  overlay: SceneOverlay;
  // prop placed in the scene. `size` is now a fraction of pet size
  // (0.4 = 40% of the pet sprite width). Renderer scales accordingly.
  prop?: { sprite: SceneSpriteId; x: number; size: number; floorOffset?: number };
  petAnim: "eat" | "chase" | "shake" | "run" | "happy" | "sleep" | "jump";
  particle: "heart" | "star" | "drop" | "zzz" | "leaf" | "sparkle";
  petAnchor?: number;
  // Pet's vertical anchor (depth, 0 = back, 1 = front). When set, the
  // pet is positioned at this y for the duration of the scene — used
  // by wash to put the pet *inside* the bathtub at the very front.
  petAnchorY?: number;
  // hand prop floats above the pet
  handAbovePet?: boolean;
};

