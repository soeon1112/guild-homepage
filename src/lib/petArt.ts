// Adult sprites are drawn at 32×32 for finer detail; smaller stages
// stay at 16×16. Renderers compute pixel size from this so the body
// occupies the same display area as the smaller stages did before.
export function spriteCols(grid: PixelGrid): number {
  let max = 0;
  for (const row of grid) if (row.length > max) max = row.length;
  return max;
}

